    private void upload()
    {
        jfc = new JFileChooser();
        int a = jfc.showDialog(this, "Choose Passport");
        if(a == JFileChooser.APPROVE_OPTION)
        {
            file = jfc.getSelectedFile();
            Image im = getToolkit().createImage(file.getPath());
            im = im.getScaledInstance(120, 120, Image.SCALE_SMOOTH);
            image.setIcon(new ImageIcon(im));
        }
    }
    private void connection()
    {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hostel", "root", "");
            st = con.createStatement();
            
            rs = st.executeQuery("SELECT student_id FROM students");
            
            while(rs.next())
                matricNo.add(rs.getString(1));
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
