    private void validateInput()
    {
        if(file != null)
        {
            if(!(fullname.getText().equals("") || matric.getText().equals("") || dept.getText().equals("") || phone.getText().equals("") || email.getText().equals("") || parent.getText().equals("") || address.getText().equals("") || state.getText().equals("")))
            {
                if(matric.getText().length() == 6)
                {
                    if(!matricNo.contains(matric.getText()))
                    {
                        try {
                            //add to record
                            ps = con.prepareStatement("INSERT INTO students VALUES(?,?,?,?,?,?,?,?,?,?,?)");

                            ps.setString(2, fullname.getText());
                            ps.setString(1, matric.getText());
                            ps.setString(3, dept.getText());
                            ps.setString(4, level.getSelectedItem().toString());
                            String sex;
                            if(buttonGroup1.getSelection().getActionCommand().equals(male.getText()))
                                sex = "M";
                            else
                                sex = "F";
                            ps.setString(5, sex);
                            ps.setString(6, phone.getText());
                            ps.setString(7, email.getText());
                            ps.setString(8, parent.getText());
                            ps.setString(9, address.getText());
                            ps.setString(10, state.getText());
                            ps.setString(11, file.getPath());

                            int a = ps.executeUpdate();

                            if(a != -1)
                            {
                                JOptionPane.showMessageDialog(this, "Record Successfully saved", "Congratulation", JOptionPane.INFORMATION_MESSAGE);
                                reset();
                            }
                            else
                                JOptionPane.showMessageDialog(this, "Record not save", "Problem occur", JOptionPane.WARNING_MESSAGE);


                        } catch (SQLException ex) {
                            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    else
                        JOptionPane.showMessageDialog(this, "Matric no already exist", "Invalid Matric number", JOptionPane.WARNING_MESSAGE);
                }
                else
                    JOptionPane.showMessageDialog(this, "Invalid Matric number", "Invalid Matric number", JOptionPane.WARNING_MESSAGE);
            }
            else
                JOptionPane.showMessageDialog(this, "All fields must be fill", "Fill all Fields", JOptionPane.WARNING_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(this, "Select a valid image to use with your accout", "Choose a Valid Passport", JOptionPane.WARNING_MESSAGE);
    }
